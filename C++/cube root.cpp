// CPP program to demonstrate the cbrt()
// STL function
#include <bits/stdc++.h>
using namespace std;

int main()
{
	// cbrt() function with integral
	// argument
	int num1 = 9;
	cout << cbrt(num1) << endl;

	// cbrt() function with floating-point
	// argument
	double num2 = 7.11;
	cout << cbrt(num2) << endl;

	long long num3 = 7;
	cout << cbrt(num3);

	return 0;
}
